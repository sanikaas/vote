# swap_numbers.py

# Get input from user
a = input("Enter the first number: ")
b = input("Enter the second number: ")

print("\nBefore swapping:")
print("First number (a):", a)
print("Second number (b):", b)

# Swap the values
a, b = b, a

print("\nAfter swapping:")
print("First number (a):", a)
print("Second number (b):", b)
